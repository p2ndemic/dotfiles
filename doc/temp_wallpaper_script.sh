#!/bin/bash

# ==============================================================================
# set-random-wallpaper.sh
# Универсальный скрипт для установки случайных обоев рабочего стола.
# Поддерживает: pcmanfm-qt и swaybg.
# ==============================================================================

# Пути и настройки по умолчанию
WALLPAPER_DIR="$HOME/Pictures/Wallpapers"
BACKEND=""
MODE=""

# Режимы по умолчанию для каждого бэкенда
DEFAULT_MODE_PCManFM="zoom"
DEFAULT_MODE_SwayBG="fill"

# ------------------------------------------------------------------------------
# Функция вывода справки
# ------------------------------------------------------------------------------
show_help() {
    cat << EOF
Использование: $0 [ОПЦИИ]

Обязательно укажите один из бэкендов:
  -p, --pcmanfm-qt          Использовать pcmanfm-qt (LXQt)
  -s, --swaybg              Использовать swaybg (Wayland/Sway)

Настройка режима отображения (опционально):
  --wallpaper-mode <РЕЖИМ>  Режим для pcmanfm-qt (по умолчанию: $DEFAULT_MODE_PCManFM)
                            Доступно: color, stretch, fit, center, tile, zoom
  -m, --mode <РЕЖИМ>        Режим для swaybg (по умолчанию: $DEFAULT_MODE_SwayBG)
                            Доступно: stretch, fit, fill, center, tile, solid_color

Примеры:
  $0 -p
  $0 -p --wallpaper-mode fit
  $0 -s
  $0 -s -m center
  $0 --swaybg --mode fill

EOF
}

# ------------------------------------------------------------------------------
# Парсинг аргументов
# ------------------------------------------------------------------------------
while [[ $# -gt 0 ]]; do
    case $1 in
        -p|--pcmanfm-qt)
            BACKEND="pcmanfm"
            shift
            ;;
        -s|--swaybg)
            BACKEND="swaybg"
            shift
            ;;
        --wallpaper-mode)
            MODE="$2"
            shift 2
            ;;
        -m|--mode)
            MODE="$2"
            shift 2
            ;;
        -h|--help)
            show_help
            exit 0
            ;;
        *)
            echo "Ошибка: Неизвестный аргумент '$1'"
            show_help
            exit 1
            ;;
    esac
done

# ------------------------------------------------------------------------------
# Проверка обязательных параметров
# ------------------------------------------------------------------------------
if [[ -z "$BACKEND" ]]; then
    echo "Ошибка: Необходимо указать бэкенд (-p или -s)."
    show_help
    exit 1
fi

# ------------------------------------------------------------------------------
# Установка режима по умолчанию, если не указан
# ------------------------------------------------------------------------------
if [[ -z "$MODE" ]]; then
    if [[ "$BACKEND" == "pcmanfm" ]]; then
        MODE="$DEFAULT_MODE_PCManFM"
    elif [[ "$BACKEND" == "swaybg" ]]; then
        MODE="$DEFAULT_MODE_SwayBG"
    fi
fi

# ------------------------------------------------------------------------------
# Выбор случайного изображения
# ------------------------------------------------------------------------------
# Проверка существования директории
if [[ ! -d "$WALLPAPER_DIR" ]]; then
    echo "Ошибка: Директория с обоями не найдена: $WALLPAPER_DIR"
    exit 1
fi

# Поиск случайного файла
# Используем find и shuf. Если файлов нет, переменная будет пустой.
RANDOM_IMAGE=$(find "$WALLPAPER_DIR" -type f | shuf -n 1)

if [[ -z "$RANDOM_IMAGE" ]]; then
    echo "Ошибка: В директории $WALLPAPER_DIR не найдено файлов изображений."
    exit 1
fi

echo "Выбрано изображение: $RANDOM_IMAGE"
echo "Бэкенд: $BACKEND | Режим: $MODE"

# ------------------------------------------------------------------------------
# Применение обоев
# ------------------------------------------------------------------------------
case $BACKEND in
    pcmanfm)
        # Для pcmanfm-qt используем флаги --set-wallpaper и --wallpaper-mode
        # Команда отправляет сигнал запущенному экземпляру или обновляет конфиг
        if command -v pcmanfm-qt &> /dev/null; then
            pcmanfm-qt --set-wallpaper "$RANDOM_IMAGE" --wallpaper-mode "$MODE"
            if [[ $? -eq 0 ]]; then
                echo "Обои успешно установлены через pcmanfm-qt."
            else
                echo "Ошибка при установке обоев через pcmanfm-qt."
                exit 1
            fi
        else
            echo "Ошибка: Программа pcmanfm-qt не найдена в PATH."
            exit 1
        fi
        ;;
    
    swaybg)
        # Для swaybg необходимо завершить предыдущий процесс, 
        # так как он блокирует вывод (layer shell)
        if command -v swaybg &> /dev/null; then
            # Тихо убиваем старые процессы swaybg, чтобы не было конфликтов
            pkill -x swaybg 2>/dev/null
            
            # Запускаем новый процесс. 
            # Внимание: swaybg работает в фоновом режиме (блокирует скрипт).
            # Для systemd-таймера лучше запускать в фоне (&), но тогда скрипт завершится.
            # Если скрипт вызывается сервисом Type=oneshot для обновления, 
            # нам нужно, чтобы swaybg остался висеть в фоне, а скрипт вышел.
            
            swaybg -i "$RANDOM_IMAGE" -m "$MODE" &
            
            # Даем процессу немного времени на старт перед выходом скрипта
            sleep 1 
            echo "Обои успешно установлены через swaybg (процесс запущен в фоне)."
        else
            echo "Ошибка: Программа swaybg не найдена в PATH."
            exit 1
        fi
        ;;
esac

exit 0
